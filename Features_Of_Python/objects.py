# In python, everything is an object. Objects are instances of types/classes,
# and they hold values.
# 
#   Each object has:
#     - An identity (unique and constrant, like a memory address).
#     - A type (defines the operations that can be performed on the object).
#     - A value (the data the object holds).
    
#Example: Object, Values and Types

x = 42 #object of type int with value 42
y = "Hello" #object of type str with value "Hello"
z = [1, 2, 3] #object of type list with value [1, 2, 3]

print(type(x)) #output: class = int

print(type(y)) #output: class = str

print(type(z)) #output: class = list